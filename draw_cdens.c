void main(int argc, char *argv[])
{
  int count;
  picRec graymap;
  int i, j;
  
  
  count = getargs(argc, argv, "");

    if (count < 0 || count == argc) {
    fprintf(stderr, "Usage: %s filename\n");
    exit(1);
  }

  allocate_image(&graymap, MONOMASK, 64, 6, 8);

  for (i = 0; i < 64; i++)
    for (j = 0; j < 6; j++)
      put_val(i, j, 255 - (cdens[(i / 2) + (j / 2) * 32][i % 2][j % 2] * 255) / 100, MONO, &graymap);

  save_pic("cdens.graymap", "gif", &graymap);

  exit(0);
}
